package gnu.kawa.io;

import java.net.URI;




















































































































































































































































































































































































































































class URIStringPath
  extends URIPath
{
  String uriString;
  
  public String toURIString()
  {
    return uriString;
  }
  
  public URIStringPath(URI uri, String uriString) {
    super(uri);
    this.uriString = uriString;
  }
}
